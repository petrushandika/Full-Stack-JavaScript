// 1. Print all even numbers from 1 to 20 using a while loop.
let num = 1;

while (num <= 20) {
    if (num % 2 === 0) {
        console.log(num);
    }
    num++;
}

// 2. Print the multiplication result from 1 to 10 using a while loop.
let i = 1;

while (i <= 10) {
    let result = 1;
    for (let j = 1; j <= i; j++) {
        result *= j;
    }
    console.log(`${i}: ${result}`);
    i++;
}

