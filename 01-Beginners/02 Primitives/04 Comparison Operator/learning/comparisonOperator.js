/*
Comparison Operator
- Relational Operators :
  > Grater then (>)
  > Less then (<)
  > Greater then or equal to (<=)
  > Less then or equal to (>=)

- Equality Operators
 > Strict equality (===)
 > Strict non-equality (!==)
 > Lose equality operator (==)
 > Lose not equality operator (!=)
*/

// Relational Operatorss
console.log(10 < 10);
console.log(10 > 10);
console.log(10 <= 10);
console.log(10 >= 10);

// Equality Operators
console.log(10 === 10); // type + value
console.log(10 !== 10); // type + value
console.log(10 == '10'); // value
console.log(10 != '10'); // value