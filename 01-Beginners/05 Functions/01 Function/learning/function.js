// Regular Function
// Call, Run, Execute
function regular() {
    console.log('Hello World');
}
regular();

// Function With One Parameter
function one(name) {
    console.log(`Hello ${name}`);
}
one('Ujang');

// Function With Multiply Parameter
function num(x, y) {
    return x + y;
}
const number = num(10, 20);
console.log(number);
