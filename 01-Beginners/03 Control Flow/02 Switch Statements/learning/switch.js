let x = 20;
let time;

switch(x) {
    case 0:
        time = 'Good Morning';
        console.log(time);
        break;
    case 1:
        time = 'Good Evening';
        console.log(time);
        break;
    case 2:
        time = 'Good Night';
        console.log(time);
        break
    default:
        console.log('Invalid input');
}

