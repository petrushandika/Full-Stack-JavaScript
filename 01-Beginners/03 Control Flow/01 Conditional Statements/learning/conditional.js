// Conditional Statements
let a = 20;
let b = 20;

if (a > b) {
    console.log('a greater than b');
} else if (a < b) {
    console.log('a less than b');
} else {
    console.log('a is equal to b');
}

let time = 100;
let greeting;

if (time < 25) {
    greeting = 'Good Morning';
    console.log(greeting);
} else if (time < 75) {
    greeting = 'Good Evening';
    console.log(greeting);
} else {
    greeting = 'Good Night'
    console.log(greeting);
}
