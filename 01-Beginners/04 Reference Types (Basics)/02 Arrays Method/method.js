let fruits = ['Banana', 'Strawberry', 'Apple', 'Watermelon', 'Avocado'];
let num = [3, 1, 2, 5, 4];

// push()
console.log(fruits.push('Orange'));
console.log(fruits);

// pop()
console.log(fruits.pop());
console.log(fruits);

// shift()
console.log(fruits.shift());
console.log(fruits);

// unshift()
console.log(fruits.unshift('Peach'));
console.log(fruits);

// concat()
console.log(fruits.concat(num));
console.log(fruits);

// includes()
console.log(fruits.includes('Apple'));

// join()
console.log(fruits.join('-'));

// reverse()
console.log(fruits.reverse());

// slice()
console.log(fruits.slice(0, 2));